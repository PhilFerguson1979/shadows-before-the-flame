export function getSpellBySlug(slug: string): Spell | undefined {
  return spells.find(s => s.slug === slug);
}

export function getSpellByName(name: string): Spell | undefined {
  return spells.find(s => s.name.toLowerCase() === name.toLowerCase());
}

export function getSpellsByLevel(level: number): Spell[] {
  return spells.filter(s => s.level === level);
}

export function getSpellsByClass(className: string): Spell[] {
  return spells.filter(s => s.classes.some(c => c.toLowerCase() === className.toLowerCase()));
}

export function spellNameToSlug(name: string): string {
  return name.toLowerCase().replace(/['']/g, '').replace(/[\s/]+/g, '-').replace(/[^a-z0-9-]/g, '');
}
